# if

f <- function(x, y, cond) {
        # x y y son numeros, cond es una condicion
        if (cond == "sumar") {
                out <- x + y
        } 
        if (cond == "multiplicar") {
                out <- x * y
        }
        return( out )
}

f(x = 2, y = 5, cond = "sumar")

f(x = 2, y = 5, cond = "multiplicar")

rm(f)

#-------------------------------------------------------------------------------

# if - else

f <- function(x) {
        if (x > 0) {
                out <- x
        } else {
                out <- -x
        }
        return( out )
}

f(1)

f(-1)

